--회원가입, 회원 조회에 시간을 측정하는 기능은 핵심 관심 사항이 아니다.
--시간을 측정하는 로직과 핵심 비즈니스의 로직이 섞여서 유지보수가 어렵다.
--시간을 측정하는 로직을 별도의 공통 로직으로 만들기 매우 어렵다.
--시간을 측정하는 로직을 변경할 때 모든 로직을 찾아가면서 변경해야 한다.