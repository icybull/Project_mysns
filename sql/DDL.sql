CREATE TABLE MEMBER(
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PROFILEIMG VARCHAR(255),
    EMAIL VARCHAR2(255),
    NAME VARCHAR2(255),
    NICK VARCHAR2(255),
    PWD VARCHAR2(255),
    ADDRESS VARCHAR2(255),
    PRIMARY KEY(ID)
);
CREATE TABLE CONTENT(
    IDX NUMBER GENERATED BY DEFAULT AS IDENTITY,
    IMG VARCHAR2(255),
    TITLE VARCHAR2(255),
    CONTENT VARCHAR2(255),
    M_ID NUMBER,
    REG_DATETIME DATE,
    MOD_DATETIME DATE,
    PRIMARY KEY(IDX)
);
CREATE TABLE FOLLOW(
    FOLLOW_NO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FOLLOWING NUMBER,
    FOLLOWER NUMBER,
    REG_DATETIME DATE,
    PRIMARY KEY(FOLLOW_NO)
);


